=head1 SYNOPSIS

This is a prerelease version of Mint::Text, the reference
implementation of the Mint Extensible Document System.

Only L<Text::Mint::Tokenizer>, the (surprise) tokenizer component, is
included and usable in this release. More fully featured releases and
documentation will follow.

